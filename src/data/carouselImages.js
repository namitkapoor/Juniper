// Get images for a specific section of a case study
export const getProjectImages = (projectId, section = 'research') => {
  return carouselImages[projectId]?.[section] || [];
};

// Get business challenge images filtered by pain point
export const getBusinessChallengeImages = (projectId, painPointId) => {
  const businessChallengeImages = carouselImages[projectId]?.businessChallenge || [];
  
  if (!businessChallengeImages || !painPointId) {
    return [];
  }

  return businessChallengeImages.filter(img => 
    img.painPoints?.includes(painPointId)
  );
};

// Get task analysis images filtered by category
export const getTaskAnalysisImages = (projectId, taskCategory) => {
  const taskImages = carouselImages[projectId]?.taskAnalysis || [];
  
  console.log('Getting task analysis images:', {
    projectId,
    taskCategory,
    availableImages: taskImages,
    categories: taskImages.map(img => img.taskCategory)
  });
  
  return taskCategory 
    ? taskImages.filter(img => img.taskCategory === taskCategory)
    : taskImages;
};

// Get concept images for a specific concept type
export const getConceptImages = (projectId, conceptType) => {
  return carouselImages[projectId]?.concepts?.[conceptType] || [];
};